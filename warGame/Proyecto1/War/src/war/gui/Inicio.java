    //CARGAR ARCHIVO
    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivo de Capas", "map");
        jfc.setFileFilter(filter);
        int returnVal = jfc.showOpenDialog(contenedor1);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                imp.enlazarObservador(rpc);
                imp.leerCapas(jfc.getSelectedFile().getAbsolutePath(), jComboBoxCapa.getSelectedIndex());                
            } catch (IOException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jToggleButton1ActionPerformed

